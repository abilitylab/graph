func TestCosine(t *testing.T) {
	cos64 := 1.0 - Cosine(float1, float2)
	cos32 := 1.0 - Cosine32(toFloat32(float1), toFloat32(float2))
	fmt.Println("half similar: 64:", cos64, "32:", cos32, "len:", len(float1))

	cos64 = 1.0 - Cosine(float1, float3)
	cos32 = 1.0 - Cosine32(toFloat32(float1), toFloat32(float3))
	fmt.Println("no similar: 64:", cos64, "32:", cos32, "len:", len(float1))

	cos64 = 1.0 - Cosine(float3, float4)
	cos32 = 1.0 - Cosine32(toFloat32(float3), toFloat32(float4))
	fmt.Println("full similar: 64:", cos64, "32:", cos32, "len:", len(float3))
}

